package CarloMethod.model;

public class Point {
    private double xPoint;
    private double yPoint;

    public void setXPoint(double xPoint) {
        this.xPoint = xPoint;
    }

    public void setYPoint(double yPoint) {
        this.yPoint = yPoint;
    }

    public double getXPoint() {
        return xPoint;
    }

    public double getYPoint() {
        return yPoint;
    }
}
